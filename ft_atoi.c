#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int s;
	int r;

	r = 0;
	s = 1;
	i = 0;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + (str[i] - '0');
		i++;
	}
	if (r > 2147483647)
		r = -1;
	if (r < -2147483648)
		r = 0;
	return (r * s);
}
