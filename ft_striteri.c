#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;
	int				j;

	j = 0;
	i = 0;
	if (!(!s || !f))
	{
		while (s[j] != '\0')
		{
			(*f)(i, &s[j]);
			j++;
			i++;
		}
	}
}
